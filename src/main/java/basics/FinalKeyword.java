package basics;

/**
 * final: final is a keyword in Java used to apply restrictions on classes, methods, and variables.
 * When applied to a variable, it means that the variable's value cannot be changed once initialized.
 * When applied to a method, it means that the method cannot be overridden in subclasses.
 * When applied to a class, it means that the class cannot be subclassed.
 * */
public final class FinalKeyword {
    private final int myConstant = 10;

    public final void myMethod() {
        // Method implementation
    }
}
